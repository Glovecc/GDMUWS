<div id="d2-1"><h3 style="padding-bottom: 15px;">新生时期（1920-1923）</h3></div>

<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">民国九年（1920年）俄共（布）为了领导远东地区党的工作，成立了俄共（布）远东局。[10][11]。时年4月，俄共（布）远东局派出的共产国际代表维经斯基（吴廷康）受命组建中国的共产党，在翻译杨明斋的陪同下，以苏俄《生活报》记者的身份来到中国，他们在北京通过俄国汉学家译学馆俄文教习伊凤阁和鲍立维的介绍联系到李大钊，又通过李与上海的陈独秀、戴传贤（戴季陶）、张东荪建立了联系。[12]。</p>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">4月底，维经斯基在上海会见了陈独秀，向他提出组织中国共产党以及中共加入共产国际的建议，并向上海共产党人提供经费资助。8月上旬，陈独秀、李汉俊、陈望道、沈玄庐、俞秀松、李达、施存统和邵力子等人在上海陈独秀寓所组织成立了“中国共产党”（后人称“中共上海支部”或“上海共产主义小组”），陈独秀被推选为书记。（所以共产国际的刊物认为，中国共产党的成立日期是1920年8月。）</p>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">陈独秀还在另外几个城市（北京、汉口、长沙、济南、广州、东京及巴黎）中发展了组织，并组织捣毁焚烧了与其观点相左的北京晨报馆。[13]1920年5月共产国际东亚书记处在上海成立。该处下设中国、朝鲜和日本三个科。中国科的工作任务是:进行党的建设、工会建设,开展共产主义宣传、组织出版工作等。1920年8月到1921年3月间，共产国际在伊尔库茨克建立共产国际执行委员会远东书记处，负责在日本、朝鲜和中国进行宣传工作。[14]</p
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">大正十年（1921年）1月，维经斯基回到俄国，随后被共产国际任命为在伊尔库茨克成立的共产国际远东局书记处书记，负责与中国、蒙古、日本、朝鲜等国的革命者联系，指导开展革命运动。 1921年6月3日，共产国际执行委员会委员马林到达上海。与此同时，共产国际远东局书记处代表尼克尔斯基也到达上海。[15]</p>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">由于中国共产党接受共产国际提供的经费援助（为了方便在中国南方变现，很多情况下不是卢布纸币现钞，而是珠宝、钻石甚至是鸦片[16]），而被指责为“崇拜卢布，是卢布主义”。 陈独秀个人反对接受共产国际的经费援助[17]，称将要独立自主，不能受制于人。但由于现实原因，最终妥协，中国共产党的主要经费仍是由总部在莫斯科的共产国际提供。</p>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">民国十年（1921年）7月23日，在共产国际的资助下，来自各地共产主义小组的13名代表（共有57名党员）和共产国际代表马林、尼克尔斯基于上海召开第一次全国代表大会，最后一天会议移至嘉兴南湖举行，会议选举陈独秀、张国焘、李达三人组成的中央局为领导机构。这也是中共通常认可的成立日期。7月，在上海公开成立工人维权组织“中国劳动组合书记部”，并陆续成立北京、武汉、长沙、广州、济南分部，发动工人维权罢工活动。[18]</p>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">民国11年（1922年）7月，中共第二次全国代表大会在上海召开，大会代表12人，代表全国195名党员。中共二大制定了中国共产党历史上第一个党章，提出了中国共产党的纲领。大会决定，中国共产党加入共产国际，宣布“中国共产党是共产国际的一个支部”，接受俄国共产党（布尔什维克）的指导。[19]为了加强对中国共产党的控制，自民国12年至16年（1923年~1927年）大革命时期，苏共中央政治局召开了122次讨论中国革命的会议，作出了738个决议。[20]</p>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">中共一大之后，中国共产党成立了领导工人运动的中国劳动组合书记部。从1922年1月到1923年2月，领导了安源路矿工人大罢工、二七大罢工，掀起了中国工人运动的第一个高潮。在持续13个月的时间里，中国发生大小罢工100余次，参加人数达到了30万以上。其中，京汉铁路工人大罢工上演了最为惨重的一幕。</p>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">民国十年（1921年）12月，共产国际代表马林得到中国大总统孙中山同意，在张太雷的陪同下前往桂林孙中山大本营所在地，在那里停留了9天，与孙三次长谈，并广泛接触国民党的其他一些领导人。之后马林提议中共应该与国民党合作。[21]共产国际执委会又发出《给共产国际驻中国特派代表的指示》，全力支持马林推行“党内合作”[22][23]。</p>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">1922年，共产国际指示中共与孙中山和中国国民党合作，陈独秀和几乎所有中共领袖最初都表示反对。4月6日，陈独秀给维经斯基写信，提出6条意见反对马林的提议，如两党革命宗旨不同、革命依据不同、盟友不同等。他也日益对莫斯科的意志和共产国际对中共的指挥感到不满,终于勉强表示服从马林传达的共产国际指示，同意加入国民党。马林主张中国共产党同中国国民党积极合作，因此与中共领导人产生很大的矛盾。1924年，马林被召回，由维经斯基接替。</p>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">民国11年（1922年）9月，经张继介绍，孙中山亲自主盟，陈独秀、李大钊等先后正式加入国民党。1923年1月12日，共产国际作出《共产国际执行委员会关于中国共产党与国民党关系问题的决议》[24][25][26]。</p>



<div id="d2-2"><h3 style="padding-bottom: 15px;">第一次国共合作时期（1923-1927）</h3></div>
<h4 style="padding-bottom: 15px;padding-left:10px;">加入国民党</h4>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">民国12年（1923年）1月，孙中山与苏联代表越飞在上海会谈后，发表联合宣言或称孙越宣言，开始联俄容共的策略。之后2月派廖仲恺继续与苏联谈判，8月又派蒋介石到苏联考察军事。10月，聘请共产国际的鲍罗廷为顾问，负责改组国民党，使之成为类似布尔什维克式政党。中国共产党第三次全国代表大会于6月12日至20日在广州召开。此次大会的中心议题是讨论全体共产党员加入国民党的问题，通过了党的“三大”宣言和《关于国民运动及国民党的决议案》。[27]。中共三大确定了全体共产党员以个人名义加入中国国民党，并与国民党建立“革命统一战线”的方针[28][29]。</p>

<h4 style="padding-bottom: 15px;padding-left:10px;">中共系的活动</h4>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">民国13年（1924年）1月20日至30日，孙中山在广州召开了国民党第一次全国代表大会，郑重阐明了三民主义[需要第三方来源][30]。大会选举出中国国民党中央执行委员会，共产党员李大钊、谭平山、毛泽东、林祖涵、瞿秋白等10人当选为国民党中央执行委员或候补执行委员，约占委员总数的1/4。会后，在国民党中央党部担任重要职务的共产党员有：组织部长谭平山，农民部长林祖涵，宣传部代理部长毛泽东等。随后，全国大部分地区以共产党员和国民党员为骨干改组或建立了各级国民党党部[31]。国民党的“一全”也标志着第一次国共合作的正式建立。</p>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">在苏联的援助下，民国13年（1924年）5月在广州黄埔建立黄埔军校。蒋介石任校长，廖仲恺任党代表，周恩来任政治部副主任。黄埔军校与以往军校的不同，把政治教育和军事训练放到同等重要的地位，注重培养学生爱国思想和革命精神，依照苏联红军的样式。黄埔军校的成立为国民革命军奠定基础，培养了大批人才，包括林彪、徐向前等解放军名将皆出自黄埔军校。</p>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">民国15年（1926年）2月17日至3月15日，共产国际执行委员会第六次扩大会议在莫斯科举行，通过了《关于中国问题的决议》，提出中国共产党和国民党应进行土地改革[32]。11月22日至12月16日，共产国际执行委员会第七次扩大全会在莫斯科举行，通过了《关于中国形势问题的决议》[33]。斯大林在中国委员会上发表了《论中国革命的前途》的演说，提出土地革命和武装斗争在中国革命中的重要地位[34]。</p>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">1925年5月30日，中国共产党以反对帝国主义屠杀中国工人为中心口号，发动群众在上海租界举行反对帝国主义的游行示威。租界的英国巡捕在南京路上先后逮捕100多人，当场打死13人，伤数十人，制造了震惊全国的五卅惨案。在中国共产党的领导和推动下，五卅运动迅速席卷全国，从工人发展到学生、商人、市民、农民等社会各阶层，约有1700万人直接参加了运动。</p>

<h4 style="padding-bottom: 15px;padding-left:10px;">合作破裂</h4>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">孙中山去世导致的国民党权力真空和权力斗争，国民党派系丛生。中共在权力斗争中也加紧行动，控制党政要害部门，联合国民党左派，分化国民党；其领导的农民运动亦使军方利益受到了冲击。最终共产党在国民党内部的权斗中败北。</p>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">1927年4月6日，张作霖主导的北洋政府派军警突击搜查苏联大使馆，搜查出多份苏联和中共分裂中国罪证，并逮捕李大钊等中共人员，并以叛国罪起诉处死。</p>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">蒋介石识破斯大林令中共通过与国民党左派合作夺取国民党的领导权进而夺取中国政权的阴谋，奋起反苏清共，汪精卫和平分共。 民国16年（1927年）4月12日，蒋介石在上海发起清党，大规模捕杀共产党员及亲共国民党员。 1927年4月18日蒋介石在南京另立“国民政府”，与武汉国民政府对峙。1927年4月27日至5月9日中国共产党第五次全国代表大会在武汉召开，陈独秀受到批评。[35] 共产国际"五月指示"要求中共实行土地革命，[36]动员千百万农民自动没收土地，组建一支8个师或10个师的可靠军队。7月初，共产国际执行委员会向中共中央发出《关于中国革命目前形势的决定》，要求中共退出国共合作统一战线。[37]。7月12日夜，根据共产国际执行委员会的紧急指示，在苏联顾问鲍罗廷的参与下，中共中央在汉口秘密进行了改组，成立临时常务委员会，由张国焘、李维汉、周恩来、李立三、张太雷组成临时中央委员会，代行中央政治局职权，陈独秀被停职，不再参加中央领导工作。当日，鲍罗廷按照联共（布）中央政治局的决定，要陈独秀动身去莫斯科，讨论中国革命问题[38]。同日，临时中央开会讨论挽救时局的办法，决定在张发奎（北伐军第二方面总指挥）部队中发动军事暴动，在工农运动基础较好的湘、鄂、赣、粤举行秋收暴动；尽快召开一次中央紧急会议，确定党的新方针，由李维汉、张太雷、瞿秋白筹备。重视武装斗争，抓枪杆子，针锋相对地反击蒋、汪对革命力量的镇压，成为临时中央明确的指导思想。7月13日，中共中央发表《中国共产党中央委员会对政局宣言》，决定撤回参加国民政府的共产党员，准备发动军事暴动和秋收暴动[39]。 　　</p>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">1927年7月15日汪精卫在武汉召集会议，宣布停止与中国共产党的合作，第一次国共合作全面破裂。</p>


<div id="d2-3"><h3 style="padding-bottom: 15px;">武装割据时期（1927-1931）</h3></div>
<h4 style="padding-bottom: 15px;padding-left:10px;">武装暴动</h4>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">8月1日，中国共产党周恩来、刘伯承、叶挺、贺龙（其时并非中共党员）等指挥发动南昌暴动，建立了军队。8月4日，中共部队放弃南昌，南下广东，这是中共首次建立正式武装部队、也是国共双方首次武力冲突。</p>
<h4 style="padding-bottom: 15px;padding-left:10px;">武装割据的形成</h4>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">1927年9月9日毛泽东在湖南、江西边界发动秋收暴动。12月11日，共产党在叶挺、叶剑英的领导下发动广州暴动，次日宣布成立广州苏维埃政府，但随即被广东军将领张发奎镇压。叶剑英和叶挺两人在战败后化装逃到香港[40]。1928年4月28日，朱德南昌起义部队与毛泽东秋收起义部队在江西井冈山会师，并于1928年5月组成中国工农红军。随着1929年毛泽东、朱德率领井冈山红四军转战赣南、闽西地区，共产党控制区（苏区）的面积不断扩大。 1930年8月毛泽东、朱德领导的红一军团与彭德怀率领的红三军团在湖南浏阳永和会师，组成中国工农红军第一方面军。1930年10月到1931年9月，国民政府三次对中国共产党位于江西、湖南的根据地中央苏区进行围剿，均以失败告终。</p>
<h4 style="padding-bottom: 15px;padding-left:10px;">八七会议</h4>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">民国16年（1927年）8月7日中共在汉口召开“八七会议”。该党文献称：该会议确定了中国共产党武装夺取政权的总方针，并视之为根本任务；此会议改变了陈独秀过去对全党的领导路线；瞿秋白、李立三、王明、博古先后主持中共中央工作；确定八七会议到遵义会议是探索如何完成根本任务的时期。该会议中，毛泽东提出了“枪杆子里面出政权”的著名论断。此会通过了《中国共产党中央执行委员会告全党党员书》、《最近农民斗争的议决案》、《最近职工运动议决案》、《党的组织问题议决案》并追认批准了1927年7月《中国共产党中央执行委员会致中国国民党革命同志书》等文件[41]</p>
<h4 style="padding-bottom: 15px;padding-left:10px;">对中国本土派的围剿</h4>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">民国18年（1929年）时值中国和苏联因中东路事件正在中国东北开战，陈独秀反对中共“武装保卫苏联”。[42]8月13日中共中央发出第44号通告,号召全党开展反对托派斗争后,中国托派被作为“反革命”、“出卖阶级的叛徒”,从中国共产党内被清除。[43]10月俄共（布）远东局在作出的《关于共产国际执行委员会第十次全会决议的决议》中认为：“陈独秀同志表达了最右倾机会主义的，而且常常也是反革命的观点。”决议再次提到陈独秀对中东路问题的态度，认为反对“保卫苏联”的口号是“机会主义的左翼国民党观点”。[44]中共中央政治局作出《中央关于反对党内机会主义与托洛茨基主义反对派的决议》,要求陈独秀“必须立即服从中央的决议，接受中央的警告，在党的路线之下工作，停止一切反党的宣传与活动。”[45]不久以陈独秀为首的托派被中央政治局开除党籍。[46][47]1929年12月古田会议上毛泽东确立了其在红四军中的领袖地位。1931年5月1日，在陈独秀的号召下4个托派组织在上海召开了统一大会，成立了统一后的托派中央，陈独秀为总书记。</p>
<h4 style="padding-bottom: 15px;padding-left:10px;">国际派的胜出</h4>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">1931年9月共产国际远东局指示中共改组中央领导，成立临时中央政治局。</p>


<div id="d2-4"><h3 style="padding-bottom: 15px;">分离主义时期（1931-1937）</h3></div>

<h4 style="padding-bottom: 15px;padding-left:10px;">分离政权的建立</h4>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">1931年9月18日，日本军发动九一八事变。11月7日，中国共产党在苏联支援下成立中华苏维埃共和国，首都为江西省瑞金。该政权颁布了宪法，发行了货币、设计了国旗，同时将其所属控制区域称为“苏区”。 1931年12月1日，毛泽东、项英、张国焘联名于《中华苏维埃共和国中央执行委员会布告》表明在一个中国疆域内有两国：“从现在起，中国疆域内有不同的两个中国，一个是中华民国，是帝国主义的工具，另一是中华苏维埃共和国，是剥削与压迫下广大工农兵的国家。他的旗帜是打倒帝国主义，消灭地主阶级，推翻国民党军阀政府，建立苏维埃政府于全中国，为数万万被压迫被剥削的工农兵士及其他被压迫群众的利益而奋斗，为全国真正的和平统一而奋斗。……他具有绝大威权打击着国民党军阀政府由崩溃走到死灭，他一定要很快取得全中国革命的胜利。”[49]。</p>
<h4 style="padding-bottom: 15px;padding-left:10px;">分离主义的受挫</h4>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">1932年10月毛泽东在宁都会议上被解除军权。民国23年（1934年），第五次围剿战争中，国民革命军在德国顾问建议下步步为营，采取持久作战和堡垒主义的方式进攻工农红军，而中共方面，毛泽东失去军事领导权，博古、李德等执行王明“御敌于国门之外”的错误政策，导致失败。中华苏维埃共和国临时中央政府迫于国民政府方面的军事压力于1934年10月18日撤离江西瑞金苏区，开始长征。同时，在南方还留下了一部分红军坚持游击战争，史称“南方游击战争”。红军在突围过程与国民政府军多次交火，损失惨重。 1935年1月长征途中的红一方面军在遵义召开中共中央政治局的扩大会议。会议结束了王明路线在党中央的统治；张闻天替代博古出任“总负责”，周恩来在军事上最高负责，毛泽东被补选为中共中央政治局常委，形成了张闻天、周恩来、毛泽东组成的新领导集体，毛泽东恢复了对红军的指挥权。毛泽东及时调整军事方针，最终成功摆脱国军的围追堵截，于1935年10月顺利到达陕西延安，与陕北红军会合。1936年10月，红军三大主力（红一方面军、红二方面军、红四方面军）在甘肃会宁会合，长征结束。</p>


<h4 style="padding-bottom: 15px;padding-left:10px;">分离主义的重生</h4>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">中共中央通过长征从江西瑞金迁到陕北延安后，虽然摆脱了中央军，但依然危机四伏，有杨虎城的西北军，与张学良的东北军，东部和北部有阎锡山的晋绥军，西有马家军。</p>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">1936年2月，中国共产党宣布开始东征抗日，一举击溃阎锡山七个师。5月中共红军东征回师陕北后，继续坚持“停止内战，一致抗日”的主张。5月5日中国共产党发表《停战议和一致抗日通电》，通电表示：中国共产党“愿意在一个月内与所有一切进攻抗日红军的武装队伍实行停战议和，以达到一致抗日的目的”[50]，同时致书国民党中央，呼吁南京国民政府“停止内战，双方互派代表，磋商抗日救亡具体办法”[51]，以实现两党合作抗日[52]。中共于1936年10月由中国工农红军红四方面军主力2.18万人组建西路军，发动西征，意图打通经新疆与苏联的联系通道，但遭遇马家军的粉碎性打击而失败。</p>

<h4 style="padding-bottom: 15px;padding-left:10px;">分离主义的放弃</h4>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">1936年12月12日，时任西北剿匪副总司令的东北军将领张学良和时任第十七路军总指挥的西北军杨虎城在中国西安发动“兵谏”，扣押时任国民政府军事委员会委员长和西北剿匪总司令的蒋中正，并杀害保安人员、中央机关人员。最终蒋中正被迫接受“停止剿共、一同抗日”的主张而获释。国民政府被迫放弃“攘外必先安内”基本国策，停止与红军作战，抗日统一战线形式上建立，史称西安事变。</p>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">1936年，中华苏维埃人民共和国政府出于联合国民党抗击日本需要，逐步取消其割据性质，回归中华民国国民政府，将国号更改为中华苏维埃民主共和国，与中华民国靠近，意味中华民国内的苏维埃政府。1937年9月22日，“中华苏维埃民主共和国”正式宣布取消。</p>

<div id="d2-5"><h3 style="padding-bottom: 15px;">抗日统一战线时期（1937-1942）</h3></div>
<h4 style="padding-bottom: 15px;padding-left:10px;">第二次国共合作</h4>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">1937年卢沟桥事变后，中日两国终于进入全面战争的状态。8月22日至8月25日洛川会议中毛泽东指出，关于军事问题，抗日战争将是一场艰苦的持久战。毛泽东还提出红军的基本任务是：“创建根据地，牵制和相机消灭敌人，配合友军作战（主要是战略配合），保存与扩大红军，以及争取民族革命战争的领导权[53]。”红军的作战方针是独立自主的山地游击战争[53]。该会议根据毛泽东的提议，通过了《抗日救国十大纲领》[54]。</p>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">1937年9月6日，第二次国共合作展开，其最后一个政府机关“中央政府西北办事处”在延安整编为陕甘宁边区政府。成为当时的国民政府行政院的一个直辖行政区域.陕北地区的红军（约4万人）改编为国民革命军第八路军（后依抗战序列改称国民革命军第十八集团军）东渡黄河，开赴华北前线；与此同时，另有约8,000名在红军主力长征后留在原地坚持游击战争的红色游击队集中组成了国民革命军新编陆军第四军（简称新四军）对日作战。</p>
<h4 style="padding-bottom: 15px;padding-left:10px;">国共摩擦与决裂</h4>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">国共双方之间也曾经爆发过多次流血冲突，其中有晋西事变、“黄桥战役”和“皖南事变”等。国民政府不再对共产党及其军队发放军饷补给，中共及其军队已不再听从国民政府号令（尽管事实听从过，但名义上是顺从的)，开始大力发展国民政府体制外的以抗日、民主为名的事实割据政权，国共统一战线已名存实亡。</p>

<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">抗日战争结束时，中国共产党的军队发展到了130余万人，民兵也发展到260余万人，控制敌占区约一亿人口[60]。</p>

