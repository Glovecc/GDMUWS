<div id="d3"><Divider class="time" orientation="left">毛泽东时代（1942-1976）
</Divider>

<div id="d3-1"><h3 style="padding-bottom: 15px;">延安时期（1942-1946）</h3></div>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">中国共产党自1921年成立后，接受共产国际指挥。1941年6月22日苏德战争爆发，苏联无暇经营中共事务。毛泽东利用这个时机，打击中共内部以王明为首的国际派。中共文献则称该运动是为了：提高党内成员马克思列宁主义水平，肃清教条主义的影响，纠正各种非无产阶级思想，提高战斗力，以便战胜困难，夺取胜利。据此决定在中共党内开展一场普遍的整顿作风的运动。</p>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">1941年初，中共中央集中在延安的120多名高级干部学习马列著作和党的历史文献。5月19日，毛泽东作了《改造我们的学习》的报告，强调要理论联系实际，提出了党内反对主观主义的斗争任务。同年七八月间，中共中央相继作出《关于增强党性的决定》和《关于调查研究的决定》，并号召其党成员加强调查研究，克服非无产阶级思想，加强党性锻炼。9月，中共中央召开政治局扩大会议。根据该党文献，此次会议检讨了其的历史上，特别是第二次国内革命战争时期政治路线的错误，肯定了1931年初党的六届四中全会到1935年底，以王明为代表的“左”倾机会主义错误是政治路线错误；提出发动全党进行思想革命和解决理论与实际相结合的问题，同时讨论了如何使党的组织达到更进一步的统一和团结的问题。中央政治局在延安召开中共中央高级干部整风会议，整风学习从此开始。[61]这是中共延安整风的预备阶段。</p>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">1942年2月，毛泽东在延安作了《整顿党的作风》和《反对党八股》的报告，提出了整风运动的内容、方针、任务和方法。4月，中共中央宣传部发布《关于在延安讨论中央决定及毛泽东同志整顿三风报告的决定》。在延安的近万名干部普遍参加了此次学习。6月，中共中央宣传部又发出在全党进行整风的指示：《关于在党内进行整顿三风学习运动的决定》。从此，中共党内军内的整风逐步展开。根据该党文献称此次整风的内容是反对主观主义以整顿学风，反对宗派主义以整顿党风，反对党八股以整顿文风；重点是反对主观唯心主义；延安整风的方针是“惩前毖后，治病救人”，即通过学习文件，掌握精神实质，检查自己的思想和工作，发扬党内民主，充分开展批评与自我批评，具体分析错误产生的根源，找出改正错误的方法，达到既要弄清思想，又要团结同志的目的；经过普遍的学习和整顿，提高全党的马克思列宁主义水平，克服工作中的缺点和错误，达到统一思想，端正作风，加强纪律，纯洁组织，提高党的战斗力。这是延安整风运动的第二阶段。</p>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">中国共产党于1943年开始重新积极扩大自身的根据地[62]。而自1940年代开始，中国共产党内部意识到若要取得政权，唯一的机会是要将自身定位为真正不受外国势力影响的中国的政治势力[63]。因此自1941年开始以延安市为据点的中国共产党展开整风运动，排除以王明为首的第三国际代表在党内的影响，并且将毛泽东的理论与原先的马克思列宁主义视为指导思想；之后随着《关于若干历史问题的决议》通过到中国共产党第七次全国代表大会召开，毛泽东在中国共产党党内的领导地位获得确立。而随着中国抗日战争在1945年逐渐接近结束，中国共产党与中国国民党间的政治问题开始被提及[64]。1945年8月日本正式投降后，中国国民党与中国共产党两党代表于重庆市展开谈判，并于10月10日签署《双十协定》。隔年双方则召开政治协商会议并通过和平建国纲领等5个决议案，表示同意共同实现民主宪政。之后双方分别在1946年2月底达成整军方案，以及在同年3月底达成《东北停战协定》。1943年5月15日，共产国际执行委员会主席团作出《关于提议解散共产国际的决定》，并于5月25日公开宣布《解散共产国际的决议》，声言这是为了适应反法西斯战争的发展，便于各国共产党独立处理问题。5月26日，中共中央发表决定，完全同意解散共产国际。[65]</p>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">延安整风的第三阶段为总结历史经验的阶段(1943年10月至1945年4月)。在此阶段，该党高级干部重新学习中共的历史，研究、讨论、总结历史经验，检讨工作，开展批评与自我批评，弄清路线是非。1944年4月12日，毛泽东作了《学习与时局》的报告。据该党文献该报告：阐明了研究历史经验应当采取的正确方针和态度，对高级干部关于党的历史问题的讨论作了总结。1945年4月，中共六届七中全会通过了《关于若干历史问题的决议》。根据其文献此会对于该党的历史上各次“左”右倾错误，特别是第三次“左”倾错误，作出了批评和结论。至此，中共延安整风运动结束。</p>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">延安整风运动是一次全党范围内空前规模的马克思主义教育运动和思想解放运动。各机关的成员组成学习小组，来学习中央指定的二十二个文件，包括官方文件和个人著作讲话，其中以毛泽东的著作占六篇最多，而虽然也有许多国外作者如列宁、斯大林的著作，但在实行上被列为居次。延安整风彻底揭露、批判和清算了党内历次“左”右倾错误，特别是王明“左”倾错误在党内的恶劣影响，使全党的马克思列宁主义水平得到了进一步提高。延安整风后，王明在党内失去势力。全党在以毛泽东为首的中共中央领导下，达到了空前的团结，为日后与国民政府的军事和政治斗争，以及第二次国共内战的胜利建立政治基础。</p>

<div id="d3-2"><h3 style="padding-bottom: 15px;">国共内战时期（1946-1950）</h3></div>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">1945年8月抗日战争结束后不久爆发第二次国共内战，中国共产党及后来的中华人民共和国称之为“解放战争”，也称“第三次国内革命战争”；中国国民党及中华民国称之为“戡乱”。</p>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">1945年8月至10月，毛泽东、周恩来等飞赴重庆与国民政府谈判。有关问题的具体谈判主要在中共代表周恩来、王若飞和政府代表王世杰、张群、张治中、邵力子之间进行。经过激烈的争论，最终双方于10月10日签署“政府与中共代表会谈纪要”（即“双十协定”）决定在年底召开政治协商会议。1946年1月，国共两党并民盟、青年党等代表召开政治协商会议，通过了和平建国纲领等五个决议案，同意共同实现民主宪政[66][67]。 1946年2月底，虽有反苏运动等因素，国共双方仍达成整军协定，统一将国共军队整编为国军。协定划定全国驻军服务区，各军队集结在此统一整编；整编以12个月为一期，分期缩编部队，直到完成全国60个师的目标。3月底，国共达成迄今最后一个正式协议东北停战协定。 1946年3月至1947年3月，国共双方处于边打边谈的状态，双方在东北、中原、华北等处展开争夺。1946年11月15日，国民大会召开。此时，国民政府为迎接制宪国民大会，下达了第四次停战令。1947年1月，《中华民国宪法》通过之后，国民政府应美国政府代表马歇尔的要求，先后三次呼吁中共举行圆桌会谈，以政治方式解决两党争端。共产党方面认为国民政府没有诚意，中共南京代表陆定一回应，“废除伪宪法和恢复1946年1月31日军事位置，是恢复和谈的最低限度”[68]，至此国共谈判完全破裂。</p>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">马歇尔调停失败后，内战全面爆发。之后，国军调整战略，重点进攻共产党山东和陕北两大共产党控制区。在陕北，国军一度占领延安，但此时共产党已经主动撤离延安，国军占领的只是一座空城。山东方面，解放军采取运动战打击国军，避免与国军进行主力决战。之后陈毅、粟裕率领解放军华东野战军在1947年5月的孟良崮战役中重创国军，全歼国军王牌主力、五大主力之首整编第七十四师，师长张灵甫阵亡。 1947年9月至1948年9月，解放军开始攻势作战，在东北，将国军压缩至几个相互孤立的城市，为辽沈战役奠定了基础。在陕北，彭德怀部队在1947年8月初围攻榆林，钟松整编36师北上救援。随后彭德怀围点打援，发动围攻钟松的沙家店战役。刘戡全力救援，钟松逃脱。10月，彭德怀再度围攻榆林，国军已经无兵可调，不得不由蒋中正致电宁夏主席马鸿逵求援，马鸿逵命整编18师支援榆林，彭德怀再度围点打援。宁夏军与彭德怀部队在元大滩激战，双方伤亡惨重。最后彭德怀撤退。2月，彭德怀部在宜川围点打援，歼灭刘戡29军主力，刘戡自杀。4月底彭部占领延安，后撤至黄龙地区休整。中原战场局势错杂，虽然国军寻机作战，但解放军运动灵活，故国军始终处于被动地位。1948年3月，陈赓谢富治兵团乘中原国军兵力空虚，进攻洛阳，两进两出，歼灭国军守军。4月，刘邓部队进攻阜阳。经过近一年的战斗，中原地区的战事转向对中共有利的一方，国军日趋被动。5月，中共成立中原野战军，发动宛东战役，击溃国军18军。6-7月，解放军华东野战军发动豫东战役，攻克开封，歼灭整编75师，确定了中共在中原地区的优势地位。毛泽东在1948年8月称“解放战争好像爬山，现在我们已经过了山的坳子，最吃力的爬坡阶段已经过去了。”[71] 11月，在山东、华北等战场，解放军也展开了攻势。</p>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">1948年11月至1949年1月，解放军通过辽沈、淮海、平津三大战役基本上消灭国军的主力精锐，国共内战大势底定。1949年4月20日，国民政府最后拒绝在国内和平协定上签字[72]。21日，毛泽东和朱德发布了《向全国进军的命令》[73]。命令内容为：</p>
<li style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">奋勇前进，坚决、彻底、干净、全部地歼灭中国境内一切敢于抵抗的国民党反动派，解放全国人民，保卫中国领土主权的独立和完整。</li>
<li style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">奋勇前进，逮捕一切怙恶不悛的战争罪犯。不管他们逃至何处，均须缉拿归案，依法惩办。特别注意缉拿匪首蒋介石。</li>
<li style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">向任何国民党地方政府和地方军事集团宣布国内和平协定的最后修正案。对于凡愿停止战争、用和平方法解决问题者，你们即可照此最后修正案的大意和他们签订地方性的协定。</li>
<li style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">在人民解放军包围南京之后，如果南京李宗仁政府尚未逃散，并愿意于国内和平协定上签字，我们愿意再一次给该政府以签字的机会。--《向全国进军的命令》</li>

<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">1949年4月23日解放军攻入南京，中华民国政府开始四处迁徙。解放军则向全国进军，逐步攻占了除台湾和少数沿海岛屿外的全部国土。1949年10月1日中国共产党在北京建立中华人民共和国，12月7日，中国国民党领导的中华民国政府被迫撤往台湾。</p>

<div id="d3-3"><h3 style="padding-bottom: 15px;">中共政权确立时期（1949-1954）</h3></div>

<h4 style="padding-bottom: 15px;padding-left:10px;">人民共和国的建立</h4>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">经过数月的筹备，第一届中国人民政治协商会议全体会议于1949年9月21日至30日在北平召开，通过了中国人民政治协商会议组织法、中华人民共和国中央人民政府组织法和具有临时宪法性质的共同纲领，决定以北平为国都并改名北京、以公元纪年、以《义勇军进行曲》为代国歌、以五星红旗为国旗，通过了由180人组成的第一届人民政协全国委员会名单，并选举毛泽东为中央人民政府主席、其他6位为副主席、以及另外56位为中央人民政府委员。同年9月21日中国人民政治协商会议正式宣布中华人民共和国成立[74]。10月1日，毛泽东在北京宣告中华人民共和国中央人民政府成立；12月7日，中华民国政府正式迁往台北，并继续统治台湾本岛及福建离岛地区金门、马祖(通称为台澎金马地区)至今。至此，中国历史上海峡两岸分别统治的政治格局，正式形成。</p>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">1971年10月25日联合国通过2758号决议，决定由中华人民共和国政府取代中华民国政府获得在联合国代表中国的唯一合法席位。台湾海峡两岸关系虽有发展，但两岸在意识形态及台湾的政治地位问题上立场歧异，继续处于分治的状态。</p>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">中华人民共和国建立伊始，中国历经连年战争已近成废墟；按照中共论点，“从1949年10月中华人民共和国成立到1956年，中国共产党领导全国各族人民有步骤地实现从新民主主义到社会主义的转变，迅速恢复了国民经济并开展了有计划的经济建设，在全国绝大部分地区基本上完成了对生产资料私有制的社会主义改造与农村“土改”，抑止了战时通货膨胀。”在苏联的援助下建立起一个初步完整的工业体系，并力图普及国民教育与建立基层医疗保障体系。外交上奉行完全倾向社会主义阵营（向苏联“一边倒”）的政策，援助各社会主义国家和新兴独立国家的共产主义活动。军事上，1950年基本消灭了国民政府在中国大陆的残余力量；同年，北朝鲜进攻韩国，朝鲜战争爆发。联合国军仁川登陆后，击败朝鲜人民军，并推进至中朝边境。期间，曾多次轰炸中国东北[75]。10月中共组建中国人民志愿军奔赴朝鲜，与朝鲜人民军一起与联合国军作战，使战线重回三八线，于1953年签署停火协议。朝鲜战争使美国认识到中共的军事能力，但也为中共和美国的关系的发展蒙上阴影，深刻影响了两岸问题及中华人民共和国的后续发展方向。</p>

<h4 style="padding-bottom: 15px;padding-left:10px;">政治领域的政权巩固</h4>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">1950年5月1日至1950年底，在全党进行一次大规模的整风运动。主要是在领导干部中克服骄傲自满、以功臣自居的思想，官僚主义、命令主义的作风，改善党群关系；以及少数人的腐化堕落、违法乱纪。[77]</p>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">1951年2月至1953年进行整党。</p>

<h4 style="padding-bottom: 15px;padding-left:10px;">经济领域的政权巩固</h4>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">中共称三反五反运动是1951年底到1952年10月，在中共党政机关工作人员中开展的“反贪污、反浪费、反官僚主义”和在私营工商业者中开展的“反行贿、反偷税漏税、反盗骗国家财产、反偷工减料、反盗窃国家经济情报”的政治运动的统称[78]。</p>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">1950年中国共产党七届三中全会以后，中共中央和中华人民共和国中央人民政府为了恢复国民经济，采取了一系列的政治经济措施，其中之一是调整工商业中的公私、劳资、产销关系。该党称此次调整后，资本主义工商业得到迅速发展；但资本家中的不法分子不满足于用正常方式获得一般利润，力图利用和国营经济的联系，以行贿、偷税漏税、盗骗国家财产、偷工减料、盗窃国家经济情报等手段牟取暴利，企图抗拒社会主义国营经济的领导，削弱国营经济；他们在经济上给国家造成重大损失，在政治上、思想上腐蚀了工人阶级和国家工作人员。</p>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">1951年10月，中共中央召开政治局扩大会议，决定在实际控制区内开展增产节约运动，促进经济建设。在运动中，揭发出大量的贪污盗窃现象，而贪污盗窃又与中共和其政府内部存在的严重官僚主义紧密相联。因此，中共中央在1951年12月1日和8日分别发出《关于实行精兵简政，增产节约，反对贪污，反对浪费和反对官僚主义的决定》及《关于反贪污斗争必须大张旗鼓地去进行的指示》，强调必须把“三反”斗争看作像镇压反革命运动一样重要，要大张旗鼓地发动广大群众。从此，“三反”运动在其各地区各系统各部门迅速展开。1952年1月4日，中共中央又发出《关于立即限期发动群众开展“三反”斗争的指示》 [79]，要求各单位限期发动群众开展斗争。1月5日，周恩来在中国全国政协第三十四次常委会上作了《“三反”运动与民族资产阶级》的讲话，号召全国工商界积极参加“三反”运动。在“三反”运动中，一些单位揭发出私人工商业者的贪污、行贿偷税漏税等违法行为。为此，中共中央于１月５日发出了《关于在“三反”斗争中惩办犯法的私人工商业者和坚决击退资产阶级猖狂进攻的指示》。3月11日，中国政务院公布了《关于处理贪污、浪费及克服官僚主义错误的若干规定》。3月28日，中国政务院通过了《中华人民共和国惩治贪污条例》。文件根据“严肃与宽大相结合、改造与惩治相结合”的方针，规定了对贪污分子的处理办法。</p>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">该国官方文献称：在三反运动中，又暴露出大量的贪污盗窃与社会上不法资本家的行贿、偷税漏税、盗骗国家财产、偷工减料、盗窃国家经济情报的“五毒”行为密切相联，要彻底铲除“三害”，就必须反掉“五毒”。为此，1952年1月26日，中共中央发出了《关于在城市中限期展开大规模的坚决彻底的“五反”斗争的指示》，要求向违法资本家开展一场大规模的“五反”运动。2月上旬，五反运动从各大城市开始，很快在全国大张旗鼓地开展起来。3月11日，中国政务院批准公布了北京市人民政府《在“五反”运动中关于工商户分类处理的标准和方法》。要求：把私营工商户分为守法户、基本守法户、半守法半违法户、严重违法户、完全违法户５类进行定案处理。10月中共中央批准了安子文、廖鲁言关于结束“三反”、“五反”运动的两个报告，“三反”和“五反”运动结束[80]。</p>


<div id="d3-4"><h3 style="padding-bottom: 15px;">社会主义建设、教育时期（1954-1966）</h3></div>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">大跃进是在1958至1960年上半年，在中国共产党领导下，于中华人民共和国发生的试图利用本土充裕劳动力和蓬勃的群众热情在工业和农业上“跃进”的社会主义建设运动。 由于1956年中华人民共和国生产资料私有制的社会主义改造基本完成，1957年又完成了发展国民经济的第一个五年计划。1958年至1960年，中国共产党发动主张“超英赶美”的大跃进，但反而造成国民经济的倒退和大饥荒的发生。其中1959年庐山会议上，毛泽东因为彭德怀等人的异议而下令发动反右倾运动，进而造成社会经济陷入困顿，并且一直到1962年七千人大会召开后才得以制止。与此同时，中国共产党和苏联共产党因为意识形态差异而宣告分裂[82]。</p>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">反右倾运动亦称“反右倾斗争”，是1959年庐山会议后随即展开的一场政治运动。1959年庐山会议中，中华人民共和国国防部部长彭德怀、中共中央政治局候补委员张闻天等反对毛泽东发动的大跃进运动有较大异议的中共党员被打倒，会后毛泽东发动“反右倾”斗争。大量中共党员遭到批判，部分被定性为“右倾机会主义分子”受到组织处分，大跃进运动温度不降反升，造成更多的人饿死。据1962年甄别平反时的统计，被重点批判和定为右倾机会主义分子的干部和党员有三百几十万人。中共后来认为这些干部和党员大都是敢于讲真话、敢于反映真实情况和敢于提出批评意见的，运动是错误的。</p>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">四清运动，简称“四清”，是1962年底中共八届十中全会以后由毛主席在中国农村逐步推开的一场政治运动，意图“反修防修”，防止演变。四清运动最初是“清工分，清账目，清财物，清仓库”，后来扩大为“大四清”，即“清政治，清经济，清组织，清思想”。农村的四清运动与城市里的五反运动合称社会主义教育运动。</p>

