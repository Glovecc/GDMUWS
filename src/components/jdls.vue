<div id="d1"><Divider  class="time" orientation="left">成立前奏（1919-1920）</Divider>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">1840年鸦片战争以后，国际帝国主义势力陆陆续续侵入中国，使得社会结构从封建社会逐步演变为半殖民地暨半封建社会，在这过程中陆续发生太平天国运动以及辛亥革命。与此同时现代工业技术的发展使得中国出现无产阶级并且不断成长，到了1919年时工人人数已经达到200万人左右。民国八年（1919年）3月共产国际成立，通过了《共产国际行动纲领》、《关于资产阶级民主和无产阶级专政的提纲》、布哈林起草的《共产国际行动纲领》和托洛茨基起草的《共产国际致全世界无产者宣言》，鼓励无产阶级推翻资产阶级的统治，建立无产阶级专政[1]。中国共产党的想法最早起源自1919年五四运动以及所引起的新文化运动，当时许多改进中国社会的主张引起许多讨论，而无政府主义、共产主义等激进意识形态也被中国知识分子引入，并且和工人运动开始结合[2]。不过早在1917年俄国十月革命成功后，有关共产主义或者社会主义的思想在中国成为热门。其中许多主张共产主义可以改善中国社会的学者在新文化运动期间，于中国各地建立起共产主义研究小组[3]。与此同时，李大钊则成为首位公开支持列宁主义与世界革命的中国知识分子[4]。1919年3月俄国第三国际宣告成立，鼓励全世界无产阶级推翻资产阶级的统治，进而建立无产阶级专政[5]。到了隔年，苏联共产党领导的第三国际成立共产国际远东局。</p>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">1920年初，李大钊和陈独秀等人开始有关于建立共产党讨论。1920年4月，共产国际远东局派遣吴廷康等人作为代表前往观察中国状况，计划在上海市建立第三国际东亚书记处并且在中国组建共产党。吴廷康随后在翻译杨明斋陪同下先是前往北京市与李大钊会面，之后则到访上海市和陈独秀共同商讨建党问题。同年5月开始，陈独秀邀请李汉俊、李达、俞秀松等共产主义支持者共同商讨建党事宜。同年8月，陈独秀、李汉俊、李达、陈望道、俞秀松等人在上海市组织成立了中国共产党，并且推选陈独秀为书记，后来这在上海市组建的首个中国共产党组织被称为“上海共产主义小组”。上海共产党早期组织成立后，实际上成为中国各地建党活动的联络中心。1920年10月，李大钊、张申府和张国焘发起起成立北京共产党早期组织，并且由李大钊担任负责人。而在早期组织建立后，包括罗章龙、刘仁静、邓中夏、高君宇、何孟雄、缪伯英、范鸿劼、张太雷等北京大学马克思学说研究会成员先后加入。[6]</p>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">尽管李大钊和陈独秀就是否要在中华民国政坛从事政治改革之论点有所差异[7]，然而两人都把俄国十月革命作为开创性的行动，认为它预示著许多被压迫的国家即将进入新的时代[7]。1920年秋季，施存统、周佛海等人在日本东京建立旅日共产党早期组织，由施存统为负责人。同年秋冬之际，毛泽东、何叔衡等人在长沙市以新民学会骨干为核心，秘密组建共产党早期组织。1920年年底至1921年年初，王尽美、邓恩铭等人则在济南市建立共产党早期组织。在与无政府主义者组织的共产党决裂后，1921年春季陈独秀联合谭平山、陈公博、谭植棠等人重新组建广州共产党早期组织，并且先后由陈独秀和谭平山担任书记。同一年张申府、周恩来、赵世炎、刘清扬等人在法国巴黎也建立了由留学生组成的共产党早期组织，并以张申府为负责人。这些早期组建后来中国共产党的地方组织并未统一名称，其名称包括有“共产党”、“共产党支部”或者是“共产党小组”，后来则被通称为“共产主义小组”。这些在各地成立的共产主义小组最早参考了列宁所提出的先锋主义运作[8]，并且被蔡和森等学术界视为中国共产党的雏形[9]。</p>
<p style="padding-bottom: 15px;text-indent: 2em;font-size: 15px;">这些共产主义工作小组成员有组织且有计划地扩大有关马克思主义的研究和宣传，并且批判各类反对马克思主义的思想；同时还在各个地方还发起建立社会主义青年团、创办工人刊物、开办工人学校，并且领导工人成立工会以发动工人运动，这些进一步促进了马克思主义理论和工人运动的结合。其中在1920年9月，上海共产主义小组把《新青年》杂志改为自身公开刊物，同年11月又创办了在中国各个主要城市秘密发行的《共产党》月刊。新青年出版社还翻译《共产党宣言》、《国家与革命》等马克思列宁主义著作，以及发行多份宣传马克思主义的小册子。各地共产主义小组也积极创办了以工人为主要读者的通俗刊物，借此对工人进行阶级意识的启蒙教育，这包括有上海市发行的《劳动界》，北京市发行的《劳动音》和《工人月刊》、济南市发行的《济南劳动月刊》、广州市发行的《劳动者》等。各地共产主义小组以此为基础积极深入工人群众，进而举办工人夜校和建立工会组织，同时还在各地组织社会主义青年团。</p>
